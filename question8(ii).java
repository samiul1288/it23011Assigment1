public class q8 {
    public static void main(String[] args)
    {
        int [] A={1,2,3,4,5};
        printArray(A);
    }
    public static void printArray(int[] arr)
    {
        System.out.print("Array Element: ");
        for(int n:arr){
            System.out.print(n+" ");
        }
    }
}
